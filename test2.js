// creates function called "square" which takes a single argument of "x" and then squares the number.
function square(x) {
	return x * x
	}
// Prints result of function with argument "2"
console.log(square(2))
// creates function called "cube" which takes a single argument of "y" and then cubes the number.
var cube = function(y) {
	return y * y * y
}
// Prints the result of function with argument "2"
console.log(cube(2))
